const mongoose = require('mongoose');

const trainerSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    password: {
        type: String
    }

})

module.exports = mongoose.model('trainer', trainerSchema);